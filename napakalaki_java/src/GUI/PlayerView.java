    public void fillTreasurePanel (JPanel aPanel, ArrayList<Treasure> aList) {
            // Se elimina la informaci칩n antigua
            aPanel.removeAll();
            // Se recorre la lista de tesoros construyendo y a침adiendo sus vistas
            // al panel
            for (Treasure t : aList) {
                TreasureView aTreasureView = new TreasureView();
                aTreasureView.setTreasure (t);
                aTreasureView.setVisible (true);
                aPanel.add (aTreasureView);
            }
// Se fuerza la actualizaci칩n visual del panel
            aPanel.repaint();
            aPanel.revalidate();
    }
    
    public void setPlayer(Player p){
        playerModel=p;
        Nombrejug.setText((playerModel.getName()));
        NivelJu.setText(Integer.toString(playerModel.getLevels()));
        fillTreasurePanel (TesorosVisibles, playerModel.getVisibleTreasures());
        fillTreasurePanel (TesorosOcultos, playerModel.getHiddenTreasures());
        repaint();
        revalidate();
              
    }
